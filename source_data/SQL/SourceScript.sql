create database source_data;

use source_data;

create table mata_kuliah(
	ID_MK int not null,
	kode_mk varchar(6),
	nama_MK varchar(255),
	sks int,
	primary key(ID_MK)
);


create table dosen(
	ID_dosen int not null,
	nama_dosen varchar(255),
	inisial varchar(2),
	primary key (ID_dosen)
);	

create table mahasiswa(
	nrp varchar(10) not null,
	nama_mahasiswa varchar(255),
	ipk double(3,2),
	ips double(3,2),
	id_dosen_wali int,
	primary key (nrp),
	foreign key (id_dosen_wali) references dosen(ID_dosen)
);


create table kelas(
	ID_kelas int not null,
	ID_MK int,
	ID_dosen int,
	kelas varchar(255),
	primary key (ID_kelas),
	foreign key (ID_MK) references mata_kuliah(ID_MK),
	foreign key (ID_dosen) references dosen(ID_dosen)	
);

create table frs(
	ID_FRS int not null,
	nrp varchar(10),
	jumlah_sks int,
	tanggal_persetujuan date,
	primary key (ID_FRS),
	foreign key (nrp) references mahasiswa(nrp)
);

create table frs_kelas(
	ID_FRS int not null,
	ID_kelas int not null,
	foreign key (ID_FRS) references frs(ID_FRS),
	foreign key (ID_kelas) references kelas(ID_kelas)
);

